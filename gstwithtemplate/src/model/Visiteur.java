package model;

public class Visiteur {
	private String numVisiteur;
	private String nomVisiteur;
	private String adresseVisiteur;
	private String provenanceVisiteur;
	public String getNumVisiteur() {
		return numVisiteur;
	}
	public void setNumVisiteur(String numVisiteur) {
		this.numVisiteur = numVisiteur;
	}
	public String getNomVisiteur() {
		return nomVisiteur;
	}
	public void setNomVisiteur(String nomVisiteur) {
		this.nomVisiteur = nomVisiteur;
	}
	public String getAdresseVisiteur() {
		return adresseVisiteur;
	}
	public void setAdresseVisiteur(String adresseVisiteur) {
		this.adresseVisiteur = adresseVisiteur;
	}
	public String getProvenanceVisiteur() {
		return provenanceVisiteur;
	}
	public void setProvenanceVisiteur(String provenanceVisiteur) {
		this.provenanceVisiteur = provenanceVisiteur;
	}
	
		
}
